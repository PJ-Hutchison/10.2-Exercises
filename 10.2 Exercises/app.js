// What is the difference between var and let?
// Var can be redeclared and reassigned, let only lets you reassign. Let creates a block scope.

// What is the difference between var and const?
// Var can be redeclared and reassigned, but const can't be redeclared or reassigned. Const also creates a block scope.

// What is the difference between let and const?
// You can use let to reassign, but const can't be reassigned.

// What is hoisting?
// Hoisting is when a compiler "moves" variables to the top, so they are declared before the rest of the code runs.